public class Hexadecimal {
    public static void main(String[] args) {
        System.out.println(Integer.valueOf("5", 16));
        System.out.println(Integer.valueOf("9", 16));
        System.out.println(Integer.valueOf("10", 16));
        System.out.println(Integer.valueOf("a", 16));
        System.out.println(Integer.valueOf("16", 16));
        System.out.println(Integer.valueOf("17", 16));
        System.out.println(Integer.valueOf("18", 16));
    }
}